import { permutation } from './permutation'
;('./permutation')

describe('util/permutation', () => {
  test('permutation', () => {
    // 24 values
    expect(permutation([1, 2, 3, 4])).toEqual([
      [1, 2, 3, 4],
      [1, 2, 4, 3],
      [1, 3, 2, 4],
      [1, 3, 4, 2],
      [1, 4, 2, 3],
      [1, 4, 3, 2],

      [2, 1, 3, 4],
      [2, 1, 4, 3],
      [2, 3, 1, 4],
      [2, 3, 4, 1],
      [2, 4, 1, 3],
      [2, 4, 3, 1],

      [3, 1, 2, 4],
      [3, 1, 4, 2],
      [3, 2, 1, 4],
      [3, 2, 4, 1],
      [3, 4, 1, 2],
      [3, 4, 2, 1],

      [4, 1, 2, 3],
      [4, 1, 3, 2],
      [4, 2, 1, 3],
      [4, 2, 3, 1],
      [4, 3, 1, 2],
      [4, 3, 2, 1]
    ])

    // 12 values
    expect(permutation([1, 2, 3, 4], 2)).toEqual([
      [1, 2],
      [1, 3],
      [1, 4],

      [2, 1],
      [2, 3],
      [2, 4],

      [3, 1],
      [3, 2],
      [3, 4],

      [4, 1],
      [4, 2],
      [4, 3]
    ])

    expect(permutation([1], 2)).toEqual([])
  })
})
